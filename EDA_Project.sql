-- 5-Magnitude 
---------------
-- find total customer for countries
SELECT 
	country,
	COUNT (DISTINCT customer_number) AS Customer_Number
FROM dim_customers
GROUP BY country
ORDER BY customer_number DESC
-- find total customer by gender
SELECT 
	COUNT (DISTINCT customer_number) AS Customer_Number, 
	gender
FROM dim_customers
GROUP BY gender
-- find total products by category
SELECT 
	COUNT (DISTINCT product_number) AS Product_Numbers,
	category
FROM dim_products
GROUP BY category
-- what is the average costs in each category?
SELECT AVG(cost) AS Avg_cost , category
FROM dim_products
GROUP BY category
-- what is the total revenue generated for each category?
SELECT 
category ,
SUM([sales_amount]) AS total_revenue 
FROM fact_sales f
LEFT JOIN dim_products d
	ON f.product_key = d.product_key
GROUP BY category
ORDER BY total_revenue DESC
-- find total  revenue is generated by each customer
select  
d.customer_key,
d.first_name,
d.last_name,
SUM(f.sales_amount) AS Total_Revenua
from dim_customers as d
LEFT JOIN fact_sales as f
	on d.customer_key = f.customer_key
GROUP BY 
d.customer_key,
d.first_name,
d.last_name
ORDER BY Total_Revenua DESC
-- what is the distribution of sold items across cuontries ?
 SELECT 
	d.country,
	SUM (f.quantity) AS sold_items
 FROM dim_customers d
 LEFT JOIN fact_sales f
	ON d.customer_key = f.customer_key
GROUP BY d.country
ORDER BY sold_items DESC

-- 6- Rankig Analysis 
----------------------
-- Which 5 products generate the highest revenue ?
SELECT TOP 5
product_name ,
SUM([sales_amount]) AS total_revenue 
FROM fact_sales f
LEFT JOIN dim_products d
	ON f.product_key = d.product_key
GROUP BY product_name
ORDER BY total_revenue DESC

--What are the 5 worst-preforming products in terms of sales ?
